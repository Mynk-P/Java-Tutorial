public class Variables {
    public static void main(String[] args) {
        // To store values we have to used variables because there are so many things in a memmory.
        // Datatype :- int , float , char , string , long , double.
        int age = 18;
        System.out.println(age);  

        float average = 19.65f;
        System.out.println(average);

        char grade = 'A';
        System.out.println(grade);

        String name = "Mayank";
        System.out.println(name);

        long bigNum = 172878278728l;
        System.out.println(bigNum);

    }
}
