public class Operators {
    public static void main(String[] args) {
        // Arthmetic Operators :- + , - , % , * , ++ , --
        int firstNum = 123;
        int secondNum = 5662;
        System.out.println(firstNum+secondNum);
        System.out.println(firstNum-secondNum);
        System.out.println(firstNum*secondNum);
        System.out.println(firstNum%secondNum);
        System.out.println(firstNum/secondNum);
        System.out.println(firstNum++);
        System.out.println(++firstNum);
        // Assignment Operator :- = 
    }
}
